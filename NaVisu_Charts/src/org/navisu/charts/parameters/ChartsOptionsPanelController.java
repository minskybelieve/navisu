package org.navisu.charts.parameters;

import java.beans.PropertyChangeListener;
import java.beans.PropertyChangeSupport;
import java.util.ArrayList;
import java.util.List;
import java.util.prefs.Preferences;
import javax.swing.JComponent;
import org.netbeans.spi.options.OptionsPanelController;
import org.openide.util.HelpCtx;
import org.openide.util.Lookup;
import org.openide.util.NbBundle;

/**
 * 
 * @author Thibault
 */
@OptionsPanelController.SubRegistration(
        location = "NaVisu",
        displayName = "#AdvancedOption_DisplayName_Charts",
        keywords = "#AdvancedOption_Keywords_Charts",
        keywordsCategory = "NaVisu/Charts")
@NbBundle.Messages({
    "AdvancedOption_DisplayName_Charts=Charts", 
    "AdvancedOption_Keywords_Charts=charts option panel"
})
public final class ChartsOptionsPanelController extends OptionsPanelController {

    protected ChartsPanel panel;
    protected final PropertyChangeSupport pcs = new PropertyChangeSupport(this);
    protected boolean changed;
    
    @Override
    public void update() {
        getPanel().load();
        changed = false;
    }

    @Override
    public void applyChanges() {
        getPanel().store();
        changed = false;
    }

    @Override
    public void cancel() {
        // need not do anything special, if no changes have been persisted yet
    }

    @Override
    public boolean isValid() {
        return getPanel().valid();
    }

    @Override
    public boolean isChanged() {
        return changed;
    }

    @Override
    public HelpCtx getHelpCtx() {
        return null; // new HelpCtx("...ID") if you have a help set
    }

    @Override
    public JComponent getComponent(Lookup masterLookup) {
        return getPanel();
    }

    @Override
    public void addPropertyChangeListener(PropertyChangeListener l) {
        pcs.addPropertyChangeListener(l);
    }

    @Override
    public void removePropertyChangeListener(PropertyChangeListener l) {
        pcs.removePropertyChangeListener(l);
    }

    private ChartsPanel getPanel() {
        if (panel == null) {
            panel = new ChartsPanel(this);
        }
        return panel;
    }

    void changed() {
        if (!changed) {
            changed = true;
            pcs.firePropertyChange(OptionsPanelController.PROP_CHANGED, false, true);
        }
        pcs.firePropertyChange(OptionsPanelController.PROP_VALID, null, null);
    }
}
